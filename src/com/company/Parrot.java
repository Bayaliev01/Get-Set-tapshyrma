package com.company;

public class Parrot {
    private String nameParrot;
    private String color;
    private int growth;

    public void setNameParrot(String userName) {
        nameParrot = userName;
    }

    public String getNameParrot() {
        return nameParrot;
    }

    public void setColor(String userColor) {
        color = userColor;
    }

    public String getColor() {
        return color;
    }

    public void setGrowth(int rost) {
        growth = rost;
    }

    public int getGrowth() {
        return growth;
    }


}
