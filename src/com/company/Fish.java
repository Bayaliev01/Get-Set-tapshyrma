package com.company;

public class Fish {
    private String types;
    private String color;
    private double weight;
    private int length;


    public void setTypes(String a) {
        types = a;
    }

    public String getTypes() {
        return types;
    }

    public void setColor(String b) {
        color = b;
    }

    public String getColor() {
        return color;
    }

    public void setWeight(double massa) {
        weight = massa;
    }

    public double getWeight() {
        return weight;
    }

    public void setLength(int dlina) {
        length = dlina;
    }

    public int getLength() {
        return length;
    }


}
